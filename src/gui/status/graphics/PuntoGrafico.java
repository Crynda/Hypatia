package gui.status.graphics;

public class PuntoGrafico {

    private double x;
    private double y;

    public PuntoGrafico(double x, double y) {
        this.x = x;
        this.y = y;
    }


    public double getX() {
        return x;
    }


    public void setX(double x) {
        this.x = x;
    }


    public double getY() {
        return y;
    }


    public void setY(double y) {
        this.y = y;
    }

}